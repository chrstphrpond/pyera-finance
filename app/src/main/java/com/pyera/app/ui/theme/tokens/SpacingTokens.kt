package com.pyera.app.ui.theme.tokens

import androidx.compose.ui.unit.dp

/**
 * Spacing system based on 4dp base unit.
 * All spacing values are multiples of 4.
 */
object SpacingTokens {
    val None = 0.dp
    val ExtraSmall = 4.dp      // 1x
    val Small = 8.dp           // 2x
    val MediumSmall = 12.dp    // 3x
    val Medium = 16.dp         // 4x Most common
    val MediumLarge = 20.dp    // 5x
    val Large = 24.dp          // 6x
    val ExtraLarge = 32.dp     // 8x
    val XXL = 48.dp            // 12x
    val XXXL = 64.dp           // 16x
}
